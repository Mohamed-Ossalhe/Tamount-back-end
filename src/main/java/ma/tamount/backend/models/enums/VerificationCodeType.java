package ma.tamount.backend.models.enums;

/**
 * Enumeration representing the types of verification codes.
 * This enum defines two types of verification codes: EMAIL and SMS.
 *
 * @author Mohamed Ossalhe
 */
public enum VerificationCodeType {
    /**
     * Represents a verification code sent via email.
     */
    EMAIL,

    /**
     * Represents a verification code sent via SMS (Short Message Service).
     */
    SMS
}

